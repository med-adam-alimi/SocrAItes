def test_streaming_chat():
    """Test the streaming chat endpoint"""
    print("\nğŸ§ª Testing streaming chat API...")
    
    try:
        response = requests.post('http://127.0.0.1:5000/api/chat/stream', 
                               json={'message': 'Tell me about absurdism', 'philosopher': 'camus'},
                               timeout=30,
                               stream=True)
        
        print(f"Status: {response.status_code}")
        
        if response.status_code == 200:
            print("âœ… Streaming response received:")
            chunk_count = 0
            for line in response.iter_lines(decode_unicode=True):
                if line and line.startswith('data: '):
                    chunk_count += 1
                    if chunk_count <= 3:  # Show first 3 chunks
                        try:
                            data = json.loads(line[6:])
                            print(f"  Chunk {chunk_count}: {data.get('type', 'unknown')} - {len(data.get('text', ''))} chars")
                        except:
                            pass
            print(f"âœ… Received {chunk_count} streaming chunks")
            return True
        else:
            print(f"âŒ Error: {response.text}")
            return False
            
    except Exception as e:
        print(f"âŒ Streaming request failed: {e}")
        return False

def test_philosophers_endpoint():
    """Test the philosophers endpoint"""
    print("\nğŸ§ª Testing philosophers API...")
    
    try:
        response = requests.get('http://127.0.0.1:5000/api/philosophers', timeout=10)
        
        print(f"Status: {response.status_code}")
        
        if response.status_code == 200:
            data = response.json()
            philosophers = data.get('philosophers', [])
            print(f"âœ… Found {len(philosophers)} philosophers:")
            for phil in philosophers:
                print(f"  - {phil}")
            return True
        else:
            print(f"âŒ Error: {response.text}")
            return False
            
    except Exception as e:
        print(f"âŒ Request failed: {e}")
        return False

